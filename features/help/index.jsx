function Section({ s, open, onToggle, query }) {
  const hl = t => {
    if (!query) return t
    const i = t.toLowerCase().indexOf(query)
    if (i < 0) return t
    return <>{t.slice(0, i)}<mark style={{background:'#fde68a',borderRadius:3,padding:'0 1px'}}>{t.slice(i, i + query.length)}</mark>{t.slice(i + query.length)}</>
  }
  return (
    <div style={{background:'#fff',border:'1.5px solid #e2e8f0',borderRadius:16,overflow:'hidden',boxShadow:open?'0 4px 20px rgba(15,23,42,.08)':'0 1px 4px rgba(15,23,42,.05)',transition:'box-shadow .2s'}}>
      <button onClick={onToggle}
        style={{display:'flex',alignItems:'center',gap:13,width:'100%',padding:'15px 17px',border:'none',background:'transparent',cursor:'pointer',fontFamily:'inherit',textAlign:'left'}}>
        <div style={{width:42,height:42,borderRadius:12,background:s.color+'18',color:s.color,display:'flex',alignItems:'center',justifyContent:'center',flexShrink:0}}>
          <i className={`ti ${s.icon}`} style={{fontSize:22}}/>
        </div>
        <div style={{flex:1,minWidth:0}}>
          <div style={{fontWeight:800,fontSize:15,color:'#0f172a'}}>{hl(s.title)}</div>
          <div style={{fontSize:12.5,color:'#94a3b8',marginTop:1}}>{hl(s.lead)}</div>
        </div>
        <i className={`ti ti-chevron-${open?'up':'down'}`} style={{fontSize:18,color:'#cbd5e1',flexShrink:0}}/>
      </button>
      {open && (
        <div style={{padding:'2px 17px 17px'}}>
          {s.steps.map(([t, d], i) => (
            <div key={i} style={{display:'flex',gap:12,padding:'11px 0',borderTop:'1px solid #f1f5f9'}}>
              <div style={{width:24,height:24,borderRadius:'50%',background:s.color,color:'#fff',fontWeight:800,fontSize:12,display:'flex',alignItems:'center',justifyContent:'center',flexShrink:0,marginTop:1}}>{i + 1}</div>
              <div style={{flex:1,minWidth:0}}>
                <div style={{fontWeight:700,fontSize:13.5,color:'#1e293b',marginBottom:2}}>{hl(t)}</div>
                <div style={{fontSize:13,color:'#64748b',lineHeight:1.55}}>{hl(d)}</div>
              </div>
            </div>
          ))}
        </div>
      )}
    </div>
  )
}

export function HelpPage({ onStartTour }) {
  const [openId, setOpenId] = useState('start')
  const [q, setQ] = useState('')
  const query = q.trim().toLowerCase()

  const shown = useMemo(() => {
    if (!query) return SECTIONS
    return SECTIONS
      .map(s => {
        const inTitle = (s.title + ' ' + s.lead).toLowerCase().includes(query)
        const steps = s.steps.filter(([t, d]) => (t + ' ' + d).toLowerCase().includes(query))
        if (inTitle || steps.length) return { ...s, steps: steps.length ? steps : s.steps }
        return null
      })
      .filter(Boolean)
  }, [query])

  return (
    <div style={{maxWidth:760,margin:'0 auto'}}>
      {/* Заголовок */}
      <div style={{background:'linear-gradient(135deg,#0f172a,#1e3a5f)',borderRadius:18,padding:'22px 22px 20px',marginBottom:16,color:'#fff'}}>
        <div style={{display:'flex',alignItems:'center',gap:12,marginBottom:8}}>
          <div style={{width:46,height:46,borderRadius:13,background:'rgba(255,255,255,.14)',display:'flex',alignItems:'center',justifyContent:'center'}}>
            <i className="ti ti-book-2" style={{fontSize:26}}/>
          </div>
          <div>
            <div style={{fontSize:20,fontWeight:900,letterSpacing:'-.3px'}}>Помощь и обучение</div>
            <div style={{fontSize:13,color:'#94a3b8'}}>Весь функционал: как начать, вести, сопровождать и закрывать</div>
          </div>
        </div>
        <div style={{display:'flex',alignItems:'center',gap:10,background:'rgba(255,255,255,.1)',borderRadius:12,padding:'10px 13px',border:'1px solid rgba(255,255,255,.15)',marginTop:12}}>
          <i className="ti ti-search" style={{color:'rgba(255,255,255,.6)',fontSize:17,flexShrink:0}}/>
          <input value={q} onChange={e=>setQ(e.target.value)} placeholder="Поиск по подсказкам: «шаблон», «этап», «WhatsApp»…"
            style={{border:'none',background:'transparent',fontSize:14,color:'#fff',flex:1,outline:'none'}}/>
          {q && <button onClick={()=>setQ('')} style={{border:'none',background:'rgba(255,255,255,.2)',color:'#fff',borderRadius:7,width:24,height:24,cursor:'pointer',flexShrink:0}}>×</button>}
        </div>
        {onStartTour && (
          <button onClick={onStartTour}
            style={{marginTop:12,display:'inline-flex',alignItems:'center',gap:7,background:'#25d366',color:'#fff',border:'none',borderRadius:11,padding:'9px 15px',fontSize:13,fontWeight:700,cursor:'pointer',fontFamily:'inherit'}}>
            <i className="ti ti-player-play"/>Запустить обучающий тур заново
          </button>
        )}
      </div>

      {/* Разделы */}
      <div style={{display:'flex',flexDirection:'column',gap:10}}>
        {shown.map(s => (
          <Section key={s.id} s={s} query={query}
            open={query ? true : openId === s.id}
            onToggle={() => setOpenId(id => id === s.id ? '' : s.id)}/>
        ))}
        {shown.length === 0 && (
          <div style={{textAlign:'center',padding:'40px 20px',color:'#94a3b8'}}>
            <i className="ti ti-mood-empty" style={{fontSize:38,display:'block',marginBottom:10,opacity:.4}}/>
            Ничего не найдено по «{q}». Попробуйте другое слово.
          </div>
        )}
      </div>

      <div style={{textAlign:'center',fontSize:12,color:'#94a3b8',margin:'20px 0 8px',lineHeight:1.6}}>
        Не нашли ответ? Спросите руководителя или техника вашего агентства.<br/>
        Эта страница всегда под рукой — раздел «Помощь» в меню.
      </div>
    </div>
  )
}

export default HelpPage
