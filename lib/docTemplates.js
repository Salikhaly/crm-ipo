// ─── Сумма прописью ──────────────────────────────────────────────────────────
const U    = ['','один','два','три','четыре','пять','шесть','семь','восемь','девять']
const UF   = ['','одна','две','три','четыре','пять','шесть','семь','восемь','девять'] // тысячи — женский род
const TEEN = ['десять','одиннадцать','двенадцать','тринадцать','четырнадцать','пятнадцать','шестнадцать','семнадцать','восемнадцать','девятнадцать']
const TENS = ['','','двадцать','тридцать','сорок','пятьдесят','шестьдесят','семьдесят','восемьдесят','девяносто']
const HUND = ['','сто','двести','триста','четыреста','пятьсот','шестьсот','семьсот','восемьсот','девятьсот']

function triad(n, fem) {
  const w = [HUND[Math.floor(n / 100)]]
  const t = n % 100
  if (t >= 10 && t <= 19) w.push(TEEN[t - 10])
  else {
    w.push(TENS[Math.floor(t / 10)])
    w.push((fem ? UF : U)[t % 10])
  }
  return w.filter(Boolean).join(' ')
}

function plural(n, one, few, many) {
  const t = n % 100
  if (t >= 11 && t <= 14) return many
  const u = n % 10
  return u === 1 ? one : (u >= 2 && u <= 4) ? few : many
}

// 1234567 → «один миллион двести тридцать четыре тысячи пятьсот шестьдесят семь тенге»
export function sumWordsKzt(num) {
  let n = Math.round(Math.abs(+num || 0))
  if (!n) return 'ноль тенге'
  const parts = []
  const bil = Math.floor(n / 1e9); n %= 1e9
  const mil = Math.floor(n / 1e6); n %= 1e6
  const th  = Math.floor(n / 1e3); n %= 1e3
  if (bil) parts.push(triad(bil), plural(bil, 'миллиард', 'миллиарда', 'миллиардов'))
  if (mil) parts.push(triad(mil), plural(mil, 'миллион', 'миллиона', 'миллионов'))
  if (th)  parts.push(triad(th, true), plural(th, 'тысяча', 'тысячи', 'тысяч'))
  if (n)   parts.push(triad(n))
  return parts.filter(Boolean).join(' ') + ' тенге'
}

