const BLANK = '____________'

export function fillDocTemplate(body, c = {}, opts = {}) {
  const money = n => n > 0 ? new Intl.NumberFormat('ru').format(Math.round(n)) : BLANK
  const map = {
    'ФИО':             c.fio   || BLANK,
    'ИИН':             c.iin   || BLANK,
    'ТЕЛЕФОН':         c.phone || BLANK,
    'ГОРОД':           c.city  || BLANK,
    'АДРЕС':           c.address || c.city || BLANK,
    'ДАТА':            opts.date || new Date().toLocaleDateString('ru-RU'),
    'ТИП_ДОГОВОРА':    opts.contractLabel || BLANK,
    'ПРОГРАММА':       opts.programLabel || c.program || BLANK,
    'СУММА':           money(+c.contractAmount || 0),
    'СУММА_ПРОПИСЬЮ':  +c.contractAmount > 0 ? sumWordsKzt(c.contractAmount) : BLANK,
    'ДОХОД':           money(+c.officialIncome || 0),
    'ПЕРВ_ВЗНОС':      money(+c.downPayment || 0),
    'ЕНПФ':            money(+c.pensionContributions || 0),
    'МЕНЕДЖЕР':        opts.manager || BLANK,
  }
  return String(body || '').replace(/\{\{\s*([А-ЯЁA-Z_]+)\s*\}\}/g, (m, k) => map[k] ?? m)
}

