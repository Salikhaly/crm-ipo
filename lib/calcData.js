// ─── Хелпер: нормализует программы из calcCfg (БД) к единому виду ────────────
// Принимает calcCfg.progs_data (массив из БД) ИЛИ null.
// Возвращает массив в едином формате. Если данных нет — PROGRAMS_FALLBACK.
export function getPrograms(calcCfg) {
  const raw = calcCfg?.progs_data
  if (!Array.isArray(raw) || raw.length === 0) return PROGRAMS_FALLBACK
  return raw.map(p => ({
    id:        p.id || p.key,
    key:       p.key || p.id,
    n:         p.n || p.name,
    name:      p.name || p.n,
    icon:      p.icon || '🏠',
    r:         Number(p.r) || 0,
    t:         Number(p.t) || 25,
    d:         p.d != null ? Number(p.d) : (Number(p.downRatio) || 0.20),
    downRatio: p.downRatio != null ? Number(p.downRatio) : (Number(p.d) || 0.20),
    grp:       p.grp || 'g',
    isNauryz:  !!(p.isNauryz ?? p.is_nauryz),
    note:      p.note || p.desc || '',
    coeff:     p.coeff,
  }))
}

// ─── Формула аннуитета ───────────────────────────────────────────────────────
// pr — сумма кредита, ra — ставка % годовых, mo — срок в месяцах
export function annuity(pr, ra, mo) {
  if (!ra) return pr / mo
  const r = ra / 12 / 100
  return pr * r * Math.pow(1 + r, mo) / (Math.pow(1 + r, mo) - 1)
}

// График платежей
export function buildSch(pr, ra, mo) {
  const r = ra / 12 / 100, pm = annuity(pr, ra, mo)
  let b = pr, ci = 0
  return Array.from({ length: mo }, (_, i) => {
    const in_ = b * r, bo = pm - in_
    b = Math.max(0, b - bo); ci += in_
    return { n: i + 1, pm, bo, in_, b, ci }
  })
}

// ─── Форматирование ──────────────────────────────────────────────────────────
export function fmtMoney(n) {
  if (!n && n !== 0) return '—'
  return new Intl.NumberFormat('ru').format(Math.round(n)) + ' ₸'
}
export function fmtK(n) { return Math.round(n).toLocaleString('ru-KZ') }
export function fmtM(n) { return fmtK(n) + ' ₸' }

