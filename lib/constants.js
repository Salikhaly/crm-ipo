// Обязательные поля для перехода на этап. Возвращает { ok, msg }
export function canMoveToStage(c, stage) {
  if (stage === 'contract' && (!c.contractType || !(+c.contractAmount > 0))) {
    return { ok:false, msg:'Сначала заполните тип и сумму договора (вкладка «Договор»)' }
  }
  if (stage === 'accompaniment' && !c.phone) {
    return { ok:false, msg:'Укажите телефон клиента перед сопровождением' }
  }
  return { ok:true }
}

