// Маршрут по типу договора; без договора / неизвестный тип → полное сопровождение.
// overrides — настроенные в админке маршруты (calc_settings.accomp_templates,
// миграция 015): { groupKey: { l, stages: [{name, do, say}] } }. Если для группы
// задан свой список этапов — он главнее хардкода.
export function getAccompTemplate(contractType, overrides) {
  const key = ACCOMP_GROUP_BY_CONTRACT[contractType] || 'full'
  const ov = overrides && overrides[key]
  // Имя этапа всегда строка: этап с пустым именем (недосмотр в админке) не должен
  // отдать объект в JSX и уронить карточку
  const stageName = s => (typeof s === 'string' ? s : String(s?.name || '')).trim() || 'Этап'
  if (ov && Array.isArray(ov.stages) && ov.stages.length) {
    return { key, l: ov.l || ACCOMP_TEMPLATES[key]?.l || key, stages: ov.stages.map(stageName), custom: ov.stages }
  }
  const t = ACCOMP_TEMPLATES[key] || ACCOMP_TEMPLATES.full
  return { key, l: t.l, stages: t.stages }
}

// Гайд этапа «что делать / что говорить»: настройка из маршрута админки → дефолт из кода
export function getStageGuide(stageName, tpl) {
  const custom = tpl?.custom?.find(s => (s.name || s) === stageName)
  const base   = STAGE_GUIDE[stageName] || {}
  const doTxt  = (custom && custom.do)  || base.do  || ''
  const sayTxt = (custom && custom.say) || base.say || ''
  if (!doTxt && !sayTxt) return null
  return { do: doTxt, say: sayTxt }
}

